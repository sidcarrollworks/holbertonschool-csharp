using System;

/*
    Namespace that holds math operations
*/
namespace MyMath
{
    /// <summary>
    /// operastions ex add, subtract, multi
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// adds two numbers
        /// </summary>
        /// <param name="a">first int</param>
        /// <param name="b">second int</param>
        /// <returns>sum of a and b</returns>
        public static int Add(int a, int b) {
            return a + b;
        }
    }
}
